package edu.upc.androidapp;

public class UnityMap {
    private String ID;
    private String VectMap;
    private String name;

    public UnityMap(){

    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getVectMap() {
        return VectMap;
    }

    public void setVectMap(String vectMap) {
        VectMap = vectMap;
    }

    public String getMapName() {
        return name;
    }

    public void setMapName(String name) {
        this.name = name;
    }
}
